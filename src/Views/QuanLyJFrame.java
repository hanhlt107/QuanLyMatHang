    private void loadTable(ArrayList<Medication> lstMedication) {
        DefaultTableModel dtm = (DefaultTableModel) tblThuoc.getModel();
        dtm.setRowCount(0);

        for (Medication medication : lstMedication) {
            Object[] rowData = {
                medication.getMedicationCode(),
                medication.getCategory(),
                medication.getMedicationName(),
                medication.getQuantity(),
                medication.getCurrentPrice(),};
            dtm.addRow(rowData);
        }
    }

    private void clearForm() {
        txtMaSP.setText("");
        txtTenSP.setText("");
        txtSoLuong.setText("");
        txtGia.setText("");
        cbbDanhMuc.setSelectedItem(null);
    }

    private void loadComboBox() {
        cbbDanhMuc.removeAllItems();
        for (Category category : categoryService.getAll()) {
            cbbDanhMuc.addItem(category);
        }
    }

    private boolean checkTrungMa(String text) {
        for (Medication medication : medicationService.getAll()) {
            if (medication.getMedicationCode().equals(text)) {
                return true;
            }
        }
        return false;
    }

    private boolean checkSo(String text) {
        Pattern p = Pattern.compile("\\d+");
        Matcher m = p.matcher(text);
        return m.matches();
    }

    private boolean checkSo0(String text) {
        Pattern p = Pattern.compile("0");
        Matcher m = p.matcher(text);
        return m.matches();
    }

    private boolean checkMa(String text) {
        Pattern p = Pattern.compile("^M\\d+$");
        Matcher m = p.matcher(text);
        return m.matches();
    }

    private Medication getFormData() {
        String maSP = txtMaSP.getText().trim();
        String tenSP = txtTenSP.getText().trim();
        String soLuong = txtSoLuong.getText().trim();
        String gia = txtGia.getText().trim();
        Category category = (Category) cbbDanhMuc.getSelectedItem();
        Integer id = medicationService.getID(maSP);
        if (maSP.length() == 0 || tenSP.length() == 0 || soLuong.length() == 0 || gia.length() == 0 || category == null) {
            JOptionPane.showMessageDialog(this, "Moi ban nhap day du thong tin");
            return null;
        }
        if (!checkMa(maSP)) {
            JOptionPane.showMessageDialog(this, "Mã thuoc bắt đầu 'M' + số");
            return null;
        }
        if (!checkSo(soLuong) || !checkSo(gia) || checkSo0(soLuong) || checkSo0(gia)) {
            JOptionPane.showMessageDialog(this, "So luong/Gia phai la so nguyen ko am");
            return null;
        }

        Medication medication = new Medication(id, maSP, tenSP, Integer.parseInt(soLuong), BigDecimal.valueOf(Long.parseLong(gia)), category);
        return medication;

    }

