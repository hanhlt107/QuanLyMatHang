    private void tblThuocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblThuocMouseClicked
        // TODO add your handling code here:
        int row = tblThuoc.getSelectedRow();
        if (row == -1) {
            return;
        }

        String maSP = (String) tblThuoc.getValueAt(row, 0);
        Category category = (Category) tblThuoc.getValueAt(row, 1);
        String tenSP = (String) tblThuoc.getValueAt(row, 2);
        Integer soLuong = (Integer) tblThuoc.getValueAt(row, 3);
        BigDecimal gia = (BigDecimal) tblThuoc.getValueAt(row, 4);

        txtMaSP.setText(maSP);
        txtTenSP.setText(tenSP);
        txtSoLuong.setText(soLuong.toString());
        txtGia.setText(gia.toString());
        int soLuongDM = cbbDanhMuc.getItemCount();
        for (int i = 0; i < soLuongDM; i++) {
            Category DM = cbbDanhMuc.getItemAt(i);
            if (DM.getId() == category.getId()) {
                cbbDanhMuc.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tblThuocMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tblThuoc.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Moi ban chon 1 ban ghi can xoa");
            return;
        }
        String maSP = (String) tblThuoc.getValueAt(row, 0);
        Integer idSP = medicationService.getID(maSP);
        int ketQua = medicationService.delete(idSP);
        if (ketQua > -1) {
            JOptionPane.showMessageDialog(this, "Thanh cong");

        } else {
            JOptionPane.showMessageDialog(this, "That bai");

        }
        loadTable(medicationService.getAll());
        clearForm();

    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:k
        Medication medication = getFormData();
        if (medication == null) {
            return;
        }
        if (checkTrungMa(txtMaSP.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Ma thuoc da ton tai");
            return;
        }
        int ketQua = medicationService.insert(medication);
        if (ketQua > -1) {
            JOptionPane.showMessageDialog(this, "Thanh cong");

        } else {
            JOptionPane.showMessageDialog(this, "That bai");

        }
        loadTable(medicationService.getAll());

        clearForm();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tblThuoc.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Moi ban chon 1 ban ghi can sua");
            return;
        }
        if (!checkTrungMa(txtMaSP.getText().trim())) {
            JOptionPane.showMessageDialog(this, "Ma thuoc ko ton tai");
            return;
        }
        String maSP = (String) tblThuoc.getValueAt(row, 0);
        Medication medication = getFormData();
        if (medication == null) {
            return;
        }
        int ketQua = medicationService.update(maSP, medication);
        if (ketQua > -1) {
            JOptionPane.showMessageDialog(this, "Thanh cong");

        } else {
            JOptionPane.showMessageDialog(this, "That bai");

        }
        loadTable(medicationService.getAll());

        clearForm();
    }//GEN-LAST:event_btnSuaActionPerformed

